      <h2>§1 Foreningens navn</h2>
      <p>Støvring eSport</p>

      <h2>§2 Foreningens hjemsted</h2>
      <p>Rebild Kommune</p>

      <h2>§3 Foreningens formål</h2>
      <p>At samle interesserede indenfor esporten, afholde åbne spilleaftener, faste træningstider, turneringer og arrangementer henvendt til denne gruppe.</p>

      <h2>§4 Medlemskab</h2>
      <p><strong>§4.1</strong> Alle, der går ind for foreningens formål, kan blive medlem. Man er medlem af foreningen, når medlemskabet er registreret.</p>
      <p><strong>§4.2</strong> Bestyrelsen kan ekskludere et medlem, der arbejder imod foreningen. Det ekskluderede medlem kan skriftligt forlange, at eksklusionen bliver prøvet på førstkommende generalforsamling.</p>

      <h2>§5 Generalforsamlingen</h2>
      <p><strong>§5.1</strong> Generalforsamlingen er foreningens øverste myndighed.</p>
      <p><strong>§5.2</strong> Bestyrelsen indkalder med minimum 1 måneds varsel alle medlemmer til den årlige generalforsamling. Generalforsamling afholdes inden udgangen af marts.</p>
      <p><strong>§5.3</strong> Forslag til dagsordenen sendes til formanden senest 14 dage før og medlemmerne skal have kendskab til den endelige dagsorden med bilag 8 dage før.</p>
      <p><strong>§5.4</strong> Stemmeret: Alle medlemmer, der er fyldt 18 år har stemmeret. Forældre til børn under 18 år har en stemme pr. barn – dog har intet medlem mere end én stemme.</p>
      <p><strong>§5.5</strong> Alle, der er fyldt 15 år, kan vælges til bestyrelsen – dog skal formand, næstformand og kasserer være fyldt 18 år.</p>
      <p><strong>§5.6</strong> Alle afgørelser vedtages med simpelt flertal – dog skal vedtægtsændringer besluttes med 2/3 flertal. Alle afstemninger afgøres ved håndsoprækning. Dog skal der være skriftlig afstemning, hvis dirigenten bestemmer det eller 3 medlemmer på generalforsamlingen ønsker det samt ved alle kampvalg.</p>
      <p><strong>§5.7</strong> Generalforsamlingens dagsorden:</p>
      <ul>
        <li>Valg af dirigent og stemmetællere</li>
        <li>Bestyrelsens beretning</li>
        <li>Fremlæggelse af regnskab til godkendelse</li>
        <li>Indkomne forslag</li>
        <li>Forslag til handlingsplan og budget</li>
        <li>Valg af bestyrelse og suppleant</li>
        <li>Valg af 2 revisorer samt en revisorsuppleant</li>
        <li>Evt.</li>
      </ul>
      <p><strong>§5.8</strong> Generalforsamlingen kan afholdes digitalt. Generalforsamlingen afholdes digitalt hvis enten bestyrelsen beslutter dette eller 20% af medlemmerne ønsker dette. Konkret skal det digitale format meddeles ved indkaldelsen til generalforsamlingen. Herunder link samt hvordan valg og afstemninger foregår.</p>

      <h2>§6 Ekstraordinær generalforsamling</h2>
      <p><strong>§6.1</strong> Et flertal i bestyrelsen kan indkalde til en ekstraordinær generalforsamling, og bestyrelsen skal indkalde til en ekstraordinær generalforsamling, når mindst 20% af medlemmerne ønsker det.</p>
      <p><strong>§6.2</strong> Indkaldelsen til den ekstraordinære generalforsamling skal ske senest 14 dage efter modtagelsen af begæringen. Med indkaldelsen sendes den endelige dagsorden. Varsling som ved den ordinære generalforsamling.</p>
      <p><strong>§6.3</strong> Forslag til dagsordenen sendes til formanden senest 14 dage før og medlemmerne skal have kendskab til den endelige dagsorden med bilag 8 dage før.</p>
      <p><strong>§6.4</strong> Stemmeret: Alle medlemmer, der er fyldt 18 år har stemmeret. Forældre til børn under 18 år har en stemme pr. barn – dog har intet medlem mere end én stemme.</p>
      <p><strong>§6.5</strong> Alle, der er fyldt 15 år, kan vælges til bestyrelsen – dog skal formand, næstformand og kasserer være fyldt 18 år.</p>
      <p><strong>§6.6</strong> Alle afgørelser vedtages med simpelt flertal – dog skal vedtægtsændringer besluttes med 2/3 flertal. Alle afstemninger afgøres ved håndsoprækning. Dog skal der være skriftlig afstemning, hvis dirigenten bestemmer det eller 3 medlemmer på generalforsamlingen ønsker det samt ved alle kampvalg.</p>

      <h2>§7 Bestyrelsen</h2>
      <p><strong>§7.1</strong> Bestyrelsen står for den daglige ledelse af foreningen mellem de årlige generalforsamlinger.</p>
      <p><strong>§7.2</strong> Bestyrelsen består af 7 medlemmer der efter generalforsamlingen fordeler posterne mellem sig.</p>
      <p><strong>§7.3</strong> 2 af de 7 pladser er forbeholdt mindreårige aktive medlemmer af foreningen.</p>
      <p><strong>§7.4</strong> Bestyrelsen vælges for 2 år og kan genvælges.</p>
      <p><strong>§7.5</strong> Der vælges hvert år en suppleant til bestyrelsen. Hvis et bestyrelsesmedlem forlader bestyrelsen i valgperioden indtræder suppleanten i stedet og bestyrelsen fordeler posterne mellem sig på ny.</p>
      <p><strong>§7.6</strong> I lige år vælges 2 voksne, 1 suppleant og 1 mindreårig og i ulige år: 3 voksne 1 suppleant og 1 mindreårig.</p>
      <p><strong>§7.7</strong> Bestyrelsen er beslutningsdygtig, når mindst halvdelen af bestyrelsens medlemmer er til stede.</p>
      <p><strong>§7.8</strong> Bestyrelsen udarbejder et aftalepapir for dets interne samarbejde og kan nedsætte arbejdsgrupper efter behov.</p>
      <p><strong>§7.9</strong> Bestyrelsens møder kan afholdes digitalt.</p>

      <h2>§8 Regnskab/økonomi</h2>
      <p><strong>§8.1</strong> Regnskabsåret er kalenderåret (regnskabsperioden er 1/1 -31/12).</p>
      <p><strong>§8.2</strong> Regnskabet føres af kassereren og skal indeholde oversigt over indtægter og udgifter samt en status. Bestyrelsen er ansvarlig for at udarbejde regnskabet.</p>
      <p><strong>§8.3</strong> Regnskabet forelægges bestyrelsen løbende samt tilses og revideres af den af generalforsamlingen valgte revisor.</p>

      <h2>§9 Dispositionsret</h2>
      <p><strong>§9.1</strong> Det er formanden eller kassereren i forening med et andet myndigt bestyrelsesmedlem, der har ret til at indgå aftaler på foreningens vegne. Bestyrelsen kan uddelegere retten til at disponere til enkelte bestyrelsesmedlemmer eller nedsatte arbejdsgrupper. Ved køb eller salg af fast ejendom skal hele bestyrelsen underskrive aftalen.</p>
      <p><strong>§9.2</strong> Der påhviler ikke foreningens medlemmer nogen personlig hæftelse for de forpligtelser, der påhviler foreningen.</p>

      <h2>§10 Vedtægtsændringer</h2>
      <p>Ændringer af vedtægterne kan vedtages på en ordinær eller ekstraordinær generalforsamling, når forslaget er indsendt til tiden og mindst 2/3 af de fremmødte stemmeberettigede stemmer for forslaget.</p>

      <h2>§11 Opløsning af foreningen</h2>
      <p><strong>§11.1</strong> Foreningen kan opløses med 3/4 flertal af de fremmødte på to generalforsamlinger, som holdes med mindst en måneds mellemrum.</p>
      <p><strong>§11.2</strong> I tilfælde af opløsning gives foreningens formue til almennyttige formål i Rebild Kommune, som besluttes på den opløsende generalforsamling.</p>

